class Calculator {
  add(a, b) {
    const total = a + b;
    return total;
  }
  subtract(a, b) {
    const total = a - b;
    return total;
  }
  multiply(a, b) {
    const total = a * b;
    return total;
  }
  divide(a, b) {
    const total = a / b;
    return total;
  }
}

module.exports = Calculator;